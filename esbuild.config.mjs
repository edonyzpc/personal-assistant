import { build as esbuildBuild, context } from 'esbuild';
import process from 'process';
import { copy } from 'esbuild-plugin-copy';
import path from 'node:path';
import { readFile } from 'node:fs/promises';

const banner =
	`/*
THIS IS A GENERATED/BUNDLED FILE BY ESBUILD
if you want to view the source, please visit the github repository of this plugin
*/
`;

const prod = (process.argv[2] === "production");

// Mark every Node builtin import as external so Obsidian Mobile WebView never tries
// to require missing builtins at runtime. Catches both `node:*` (modern) and bare names
// like `worker_threads`/`crypto` (legacy) from transitive LangChain dependencies.
// Maintained list mirrors `NODE_BUILTIN_NAMES` in scripts/audit-bundle.mjs.
const NODE_BUILTIN_BARE = /^(?:fs|path|child_process|os|crypto|stream|url|net|tls|http|https|zlib|querystring|readline|buffer|events|util|tty|dns|module|process|worker_threads|fs\/promises|stream\/promises)$/;
const externalNodeBuiltinsPlugin = {
	name: "external-node-builtins",
	setup(build) {
		build.onResolve({ filter: /^node:/ }, (args) => ({ path: args.path, external: true }));
		build.onResolve({ filter: NODE_BUILTIN_BARE }, (args) => ({ path: args.path, external: true }));
	},
};

const inlineSqliteWorkerPlugin = {
	name: "inline-sqlite-worker",
	setup(build) {
		build.onResolve({ filter: /\?worker-source$/ }, (args) => {
			const sourcePath = args.path.replace(/\?worker-source$/, "");
			return {
				path: path.resolve(args.resolveDir, sourcePath),
				namespace: "sqlite-worker-source",
			};
		});

		build.onLoad({ filter: /.*/, namespace: "sqlite-worker-source" }, async (args) => {
			const result = await esbuildBuild({
				platform: "browser",
				mainFields: ["browser", "module", "main"],
				conditions: ["browser"],
				entryPoints: [args.path],
				bundle: true,
				format: "esm",
				target: "es2020",
				logLevel: "silent",
				write: false,
				metafile: true,
				minify: prod ? true : false,
				sourcemap: false,
				loader: {
					'.ts': 'ts',
					'.js': 'js',
					'.mjs': 'js',
				},
				define: {
					"process.env.NODE_ENV": prod ? '"production"' : '"development"',
				},
			});
			return {
				contents: `export default ${JSON.stringify(result.outputFiles[0].text)};`,
				loader: "js",
				watchFiles: Object.keys(result.metafile.inputs),
			};
		});
	},
};

// Lazy wasm loader: keeps the ~1.25MB base64 payload in the bundle (no separate file —
// Obsidian plugins ship as a single main.js), but defers atob+Uint8Array decoding to first
// use. After decoding the base64 string is nulled so GC can reclaim ~1.25MB. Trade-off vs
// the previous esbuild `binary` loader: same bundle size, but plugin-load heap drops by
// ~941KB (decoded Uint8Array no longer eagerly built) and users who never touch the Memory
// feature save the full payload permanently after GC.
const lazyBinaryPlugin = {
	name: "lazy-binary-wasm",
	setup(build) {
		// No onResolve: let esbuild's default resolver walk node_modules so bare imports
		// like `@sqliteai/sqlite-wasm/sqlite3.wasm` end up with an absolute on-disk path.
		// onLoad in the default `file` namespace then handles the bytes.
		build.onLoad({ filter: /\.wasm$/ }, async (args) => {
			const bytes = await readFile(args.path);
			const base64 = bytes.toString("base64");
			return {
				contents: `
var _b64 = ${JSON.stringify(base64)};
var _decoded = null;
var _decoding = null;
export default function getSqliteWasmBinary() {
    if (_decoded !== null) return _decoded;
    var b = atob(_b64);
    _decoded = new Uint8Array(b.length);
    for (var i = 0; i < b.length; i++) _decoded[i] = b.charCodeAt(i);
    _b64 = null;
    return _decoded;
}
export function getSqliteWasmBinaryAsync() {
    if (_decoded !== null) return Promise.resolve(_decoded);
    if (_decoding !== null) return _decoding;
    _decoding = Promise.resolve().then(() => {
        if (_decoded !== null) return _decoded;
        var b = atob(_b64);
        _decoded = new Uint8Array(b.length);
        for (var i = 0; i < b.length; i++) _decoded[i] = b.charCodeAt(i);
        _b64 = null;
        return _decoded;
    });
    return _decoding;
}
`,
				loader: "js",
				watchFiles: [args.path],
			};
		});
	},
};

const mainContext = await context({
	platform: "browser",
	mainFields: ["browser", "module", "main"],
	conditions: ["browser"],
	banner: {
		js: banner,
	},
	entryPoints: ["src/main.ts"],
	bundle: true,
	external: [
		"obsidian",
		"electron",
		// Mobile-compat note: ALL `node:*` builtins are additionally marked external via
		// the `externalNodeBuiltinsPlugin` below (esbuild's `external` field accepts only
		// strings; regex matching must be done in a plugin). Obsidian Mobile WebView throws
		// when these modules are required, so this is mobile compatibility, NOT size opt.
		"@codemirror/autocomplete",
		"@codemirror/collab",
		"@codemirror/commands",
		"@codemirror/language",
		"@codemirror/lint",
		"@codemirror/search",
		"@codemirror/state",
		"@codemirror/view",
		"@lezer/common",
		"@lezer/highlight",
		"@lezer/lr"],
	format: "cjs",
	target: "es2020",
	logLevel: "info",
	sourcemap: prod ? false : "inline",
	treeShaking: true,
	outfile: "dist/main.js",
	minify: prod ? true : false,
	loader: {
		'.ts': 'ts',
		'.tsx': 'tsx',
		'.js': 'js',
		'.jsx': 'jsx',
		'.md': 'text',
		// `.wasm` is handled by `lazyBinaryPlugin` (see plugin block above) — not via the
		// `binary` loader anymore. The plugin keeps the base64 string inline but defers
		// atob+Uint8Array decoding until first use, saving ~941KB of plugin-load heap and
		// allowing GC to reclaim the base64 string after decode.
	},
	define: {
		"process.env.NODE_ENV": prod ? '"production"' : '"development"',
	},
	splitting: false,
	plugins: [
		copy({
			assets: [
				{
					from: ['./manifest.json'],
					to: ['manifest.json'],
				},
				{
					from: ['./manifest-beta.json'],
					to: ['manifest-beta.json'],
				},
				{
					from: ['./styles.css'],
					to: ['styles.css'],
				},
			],
		}),
		inlineSqliteWorkerPlugin,
		lazyBinaryPlugin,
		externalNodeBuiltinsPlugin,
	],
});

if (prod) {
	await mainContext.rebuild();
	process.exit(0);
} else {
	await mainContext.watch();
}
